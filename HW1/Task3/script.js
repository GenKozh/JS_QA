// a) Declare two variables
let variable1;
let variable2;

// b) Assign values to the variables
variable1 = "Hello";
variable2 = "World";

// c) Display the values of the variables on the screen
alert("variable1: " + variable1);
alert("variable2: " + variable2);

// d) Copy the value of one variable to another
variable2 = variable1;

// e) Display the values of the variables on the screen
alert("After copying, variable1: " + variable1);
alert("After copying, variable2: " + variable2);