// Calculate the number of seconds in an hour, a day, and a month
const secondsInHour = 60 * 60; // 60 seconds * 60 minutes
const secondsInDay = secondsInHour * 24; // 24 hours in a day
const secondsInMonth = secondsInDay * 30; // Assuming 30 days in a month

// Display the results using alert
alert(`Seconds in an hour: ${secondsInHour}`);
alert(`Seconds in a day: ${secondsInDay}`);
alert(`Seconds in a month: ${secondsInMonth}`);